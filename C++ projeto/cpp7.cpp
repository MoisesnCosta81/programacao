#include <iostream>

using namespace std;

int main() {

    int x,y;
    x = 3;
    y = 9;

    cout << "Os valores de x e y respectivamente são:" << x << "e" << y << endl;
    cout << "A soma entre x e y é: " << x+y << endl;
    cout << "A subtração entre x e y é: " << x - y << endl;
    cout << "A multiplicação entre x e y é: " << x * y << endl;
    cout << "A divisão entre x e y é: " << y / x << endl;
    cout << "O resto da divisão de y e x é: " << y % x << endl;


return 0;
}

